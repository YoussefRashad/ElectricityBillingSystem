    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        // TODO add your handling code here:
        jPanelWrongMeterCode.setVisible(false);
        jPanelWrongReading.setVisible(false);
        
        if(!jTextFieldSearchKey.getText().equals("") && !jTextFieldCalc.getText().equals("") && !jTextFieldBillReading.getText().equals("")){
            Validation v = new Validation();
            if(v.checkID(jTextFieldSearchKey.getText())){
                if(v.checkBill(jTextFieldBillReading.getText())){
                    Customer x = new Customer();
                    Customer c = x.getCustomer(Integer.parseInt(jTextFieldSearchKey.getText()));
                    if(c != null){
                        jLabelSuccessOrFail.setText("Founded And Paied A Bill ...!  :)");
                        jTextFieldCalc.setText(String.valueOf(c.CalcTheconsumption(Double.parseDouble(jTextFieldBillReading.getText()))));
                        double t = c.getTotalCollected() + Double.parseDouble(jTextFieldCalc.getText());
                        c.setTotalCollected(t);
                        double d = c.getReading()-Double.parseDouble(jTextFieldCalc.getText());
                        c.setReading(d);
                        c.EditInCustomers(Integer.parseInt(jTextFieldSearchKey.getText()));
                        resetPanel();
                    }
                    else{
                            jLabelSuccessOrFail.setText("Not Found ...! :(");
                            JOptionPane.showMessageDialog(jLabel7, "Not Found Search by Meter Code ...!\n Try Again :(");
                            resetPanel();
                        }
                }
                else{
                    JOptionPane.showMessageDialog(jLabel7, "Not valid Input for Positive Double Try Again :( ");
                    jPanelWrongReading.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(jLabel7, "Not valid Search by Meter Code Try Again :(\nHint: Search by Meter Code Length should more than 4 .. ;)");
                jPanelWrongMeterCode.setVisible(true);
            }
        }
        else
            jLabelSuccessOrFail.setText("Missing Required Meter Code ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel7MouseClicked

