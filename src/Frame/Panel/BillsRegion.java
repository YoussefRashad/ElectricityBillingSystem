    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        // TODO add your handling code here:
        Customer c = new Customer();
        ArrayList<Customer> cusotmers = c.ListAllCustomer();
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        Object rowData [] = new Object[5];
        
        for(Customer x : cusotmers){
            if(x.getGovernorate().equals("Giza")){
                rowData[0]=x.getMeterCode();
                rowData[1]=x.getEmail();
                rowData[2]=x.getRegion();
                rowData[3]=x.getAddress();
                String []seprated = x.getDate().split("#");
                rowData[4]=(20+seprated[0])+"/"+seprated[1]+"/"+seprated[2];
                model.addRow(rowData);
            }
        }
        
        
            }//GEN-LAST:event_jLabel2MouseClicked

