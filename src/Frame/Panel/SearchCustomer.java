    private void jLabel22MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel22MouseClicked
        // TODO add your handling code here:
        
        
        jPanelWrongIDEmployee.setVisible(false);
        
        if(!jTextFieldAddress.getText().equals("") && !jTextFieldSearchKey.getText().equals("") && !jTextFieldID.getText().equals("") &&
            !jTextFieldPhone.getText().equals("") && !jTextFieldPass.getText().equals("") && 
                !jTextFieldEmail.getText().equals("") && !jTextFieldMeterCode.getText().equals("") && !jTextFieldURL.getText().equals("") &&!jTextFieldUserName.getText().equals(""))
        {
            Validation v = new Validation();
            if(v.checkID(jTextFieldSearchKey.getText())){
                Customer t = new Customer();
                if(t.removeCustomer(Integer.parseInt(jTextFieldSearchKey.getText()))){
                    jLabelSuccessOrFail.setText("Deleted Successfully ...! :)");
                    resetPanel();
                }
                else{
                        jLabelSuccessOrFail.setText("Not Found ...! :(");
                        JOptionPane.showMessageDialog(jLabel23, "Not Found ID Customer ...!\n Try Again :(");
                        resetPanel();
                    }
                    }
            else{
                    jLabelSuccessOrFail.setText("Not valid ID Customer :( ");
                    JOptionPane.showMessageDialog(jLabel23, "Not valid ID Customer Try Again :(\nHint: ID Customer Length should more than 4 .. ;)");
                    jPanelWrongIDEmployee.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing required Meter Code ... !  Please, complete it before submit ...! :(");
    }//GEN-LAST:event_jLabel22MouseClicked

