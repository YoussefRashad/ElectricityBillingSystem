    private void jLabelSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelSearchMouseClicked
        // TODO add your handling code here:
        jPanelWrongIDEmployee.setVisible(false);
        
        if(!jTextFieldSearchKey.getText().equals("")){
            if(new Validation().checkID(jTextFieldSearchKey.getText())){
                Customer t = new Customer();
                Customer x = t.getCustomer(Integer.parseInt(jTextFieldSearchKey.getText()));
                if(x!=null){
                    setPanel(x);
                    jLabelSuccessOrFail.setText("Founded ...! :)");
                }
                else{
                        jLabelSuccessOrFail.setText("Not Found ...! :(");
                        JOptionPane.showMessageDialog(jLabelSearch, "Not Found ID Customer ...!\n Try Again :(");
                        resetPanel();
                    }
                    }
            else{
                    jLabelSuccessOrFail.setText("Not valid ID Customer :( ");
                    JOptionPane.showMessageDialog(jLabelSearch, "Not valid ID Customer Try Again :(\nHint: ID Customer Length should more than 4 .. ;)");
                    jPanelWrongIDEmployee.setVisible(true);
            }
        }
        else{
            jLabelSuccessOrFail.setText("Missing Required Meter Code ... ! Please, Complete it to Continue :(");
        }
    }//GEN-LAST:event_jLabelSearchMouseClicked

