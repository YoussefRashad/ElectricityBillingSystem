        jPanelWrongUserName.setVisible(false);
        jPanelNotAllowed.setVisible(false);
        jPanelWrongIDAdmin.setVisible(false);
        
        if(!jTextFieldIDAdminSearch.getText().equals("")){
            if(new Validation().checkID(jTextFieldIDAdminSearch.getText())){
                Admin a = new Admin();
                a = a.getAdmin(Integer.parseInt(jTextFieldIDAdminSearch.getText()));
                if(a!=null){
                    setPanel(a);
                    jLabelSuccessOrFail.setText("Founded ...! :) ");
                }
                else{
                        jLabelSuccessOrFail.setText("Not Found ...! :(");
                        JOptionPane.showMessageDialog(jLabelSearch, "Not Found ID Admin ...!\n Try Again :(");
                        resetPanel();
                    }
                    }
            else{
                    jLabelSuccessOrFail.setText("Not valid ID Admin :( ");
                JOptionPane.showMessageDialog(jLabelSearch, "Not valid ID Admin Try Again :(\nHint: ID Admin Length should more than 4 .. ;)");
                jPanelWrongIDAdmin.setVisible(true);
            }
        }
        else
        jLabelSuccessOrFail.setText("Missing Required ID Admin ... ! Please, Complete it before Submit  :(");
