    public void resetPanel()
    {
    jTextFieldID.setText("");
    jTextFieldUserName.setText("");
    jTextFieldEMail.setText("");
    jPasswordFieldPass.setText("");
    jPasswordFieldRePass.setText("");
    jTextFieldPhoneNo.setText("");
    jComboBox1.setSelectedIndex(0);
    jTextFieldSalary.setText("");
    
        //
        jPanelWrongEmail.setVisible(false);
        jPanelWrongID.setVisible(false);
        //
        jPanelWrongPass.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        //
        jPanelWrongSalary.setVisible(false);
        jPanelWrongUserName.setVisible(false);
        jPanelNotAllowed.setVisible(false);
        jPanelWrongRePass.setVisible(false);
    }
    private void jLabel17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel17MouseClicked
        // TODO add your handling code here:
        jPanelWrongEmail.setVisible(false);
        jPanelWrongID.setVisible(false);
        //
        jPanelWrongPass.setVisible(false);
        jPanelWrongPhoneNo.setVisible(false);
        //
        jPanelWrongSalary.setVisible(false);
        jPanelWrongUserName.setVisible(false);
        jPanelNotAllowed.setVisible(false);
        jPanelWrongRePass.setVisible(false);
        
        if(!jTextFieldEMail.getText().equals("") && !jTextFieldID.getText().equals("") && !jTextFieldPhoneNo.getText().equals("") && !jTextFieldSalary.getText().equals("") && !jTextFieldUserName.getText().equals("")&& !jPasswordFieldPass.getText().equals("") && !jPasswordFieldRePass.getText().equals(""))
        {
            //All Check ...
            Validation v = new Validation();
            if(v.checkID(jTextFieldID.getText())){
                    if(v.checkName(jTextFieldUserName.getText())){
                        if(v.checkPass(jPasswordFieldPass.getText())){
                            if(v.checkPass(jPasswordFieldRePass.getText())){
                                if(v.checkEmail(jTextFieldEMail.getText())){
                                        if(v.checkPhoneNo(jTextFieldPhoneNo.getText())){
                                            if(v.checkSalary(jTextFieldSalary.getText())){
                                                    // End Check :)
                                                    // Start Add
                                                    Admin a = new Admin();
                                                    if(!a.Dublicate(Integer.parseInt(jTextFieldID.getText()))){
                                                        a.setEmail(jTextFieldEMail.getText());
                                                        a.setGender(jComboBox1.getSelectedItem().toString());
                                                        a.setID(Integer.parseInt(jTextFieldID.getText()));
                                                        a.setPhoneNo(jTextFieldPhoneNo.getText());
                                                        a.setUserName(jTextFieldUserName.getText());
                                                        a.setSalary(Double.parseDouble(jTextFieldSalary.getText()));
                                                        if(jPasswordFieldPass.getText().equals(jPasswordFieldRePass.getText())){
                                                                a.setPassword(jPasswordFieldPass.getText());
                                                                if(a.addAdmin()){
                                                                    jLabelSuccessOrFail.setText("Added Successfull ... ! :) ");
                                                                    resetPanel();
                                                                }
                                                                else
                                                                    jLabelSuccessOrFail.setText("Failed To Insert ... ! :( ");
                                                            
                                                            }
                                                            else{
                                                                jLabelSuccessOrFail.setText("Password & Repassword not Similar ... ! :("); 
                                                                JOptionPane.showMessageDialog(jLabel17, "Not valid Password & Repassword not Similar Try Again :(");
                                                                jPanelWrongPass.setVisible(true); jPanelWrongRePass.setVisible(true);
                                                            }
                                                            //End Start & start elses for Check
                                                    }
                                                        else{
                                                                jLabelSuccessOrFail.setText("Not Allowed New ID Admin :(");
                                                                jPanelNotAllowed.setVisible(true);
                                                                JOptionPane.showMessageDialog(jLabel17, "Not Allowed ID Admin , ID Admin is Reserved...!\n Try Again :(");
                                                        }
                                        }
                                            else{
                                                JOptionPane.showMessageDialog(jLabel17, "Not valid Salary Try Again :(\nHint: Salary should more than 1000 .. ;)");
                                                jPanelWrongSalary.setVisible(true);
                                            }
                                        }
                                        else{
                                            JOptionPane.showMessageDialog(jLabel17, "Not valid Phone No Try Again :(\nHint: Phone No should  equal 11 numbers if it mobile\n or more than 8 with Code of Governorate if it Tel  .. ;)");
                                            jPanelWrongPhoneNo.setVisible(true);
                                        }
                                }
                                else{
                                    JOptionPane.showMessageDialog(jLabel17, "Not valid Input for Email Try Again :(");
                                    jPanelWrongEmail.setVisible(true);
                                }
                            }
                            else{
                                JOptionPane.showMessageDialog(jLabel17, "Not valid RePassword length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n                    Try Again :(");
                                jPanelWrongRePass.setVisible(true);
                            }
                        }
                        else{
                            JOptionPane.showMessageDialog(jLabel17, "Not valid Password length ,Should it Length\nis more than 6 OR Password has (*,/,=,+,...)\n                    Try Again :(");
                            jPanelWrongPass.setVisible(true);
                        }
                }
                    else{
                        JOptionPane.showMessageDialog(jLabel17, "Not valid User Name Try Again :(");
                        jPanelWrongUserName.setVisible(true);
                    }
            }
            else{
                JOptionPane.showMessageDialog(jLabel17, "Not valid ID Try Again :(\nHint: ID Length should more than 4 .. ;)");
                jPanelWrongID.setVisible(true);
            }
        }
        else
            jLabelSuccessOrFail.setText("Missing Required Fields ... !  Please, complete them before submit ...! :(");
    }//GEN-LAST:event_jLabel17MouseClicked

